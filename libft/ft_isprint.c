#include "libft.h"

int		ft_isprint(int c)
{
	const char *s;

	s = "!\"#$%&'()*+,-./:;<=>?@[\\]^_`{|}~ ";
	if (ft_isalnum(c))
		return (1);
	while (*s)
	{
		if (*s++ == c)
			return (1);
	}
	return (0);
}
